class Cat {
  constructor(name, sex) {
    this.name = name;
    this.sex = sex;
  }
  speak() {
    `${name} says meow!`
  }
}

class Dog {
  constructor(name, sex) {
    his.name = name;
    this.sex = sex;
  }
  speak() {
    `${name} says woof!`
  }
}

class Bird {
  constructor(name, sex) {
    his.name = name;
    this.sex = sex;
  }

  speak() {
    return this.sex === 'male' ? `It's me! ${name}, the parrot!` : `${name} says squawk!`
  }
}
